# Использовать функции из задач 1, 2 для решения задания 2 из урока 2: *Программа, которая считывает два числа и выводит их сумму*

def sum():
    m = int(input('Введите первое число: '))
    n = int(input('Введите второе число: '))
    return m + n


print(sum())
